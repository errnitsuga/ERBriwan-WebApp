export const CITY_TO_BARANGAYS: Record<string, string[]> = allLocations.reduce(
  (acc, item) => {
    const existing = acc[item.city] || [];
    if (!existing.includes(item.barangay)) {
      existing.push(item.barangay);
    }
    acc[item.city] = existing;
    return acc;
  },
  {} as Record<string, string[]>,
);

