const buildNumberedBarangays = (
  city: string,
  start: number,
  end: number,
  extras: string[] = [],
) => {
  const numbered = Array.from({ length: end - start + 1 }, (_, index) => {
    return `Barangay ${index + start}`;
  });

  return [...numbered, ...extras].map((barangay) => ({
    city,
    barangay,
  }));
};

